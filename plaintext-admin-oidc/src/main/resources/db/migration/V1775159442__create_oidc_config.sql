-- OIDC Configuration table
CREATE TABLE IF NOT EXISTS OIDC_CONFIG (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME VARCHAR(200) NOT NULL,
  ENABLED BOOLEAN NOT NULL DEFAULT FALSE,
  ISSUER_URL VARCHAR(1000) NOT NULL,
  CLIENT_ID VARCHAR(500) NOT NULL,
  CLIENT_SECRET VARCHAR(1000) NOT NULL,
  SCOPES VARCHAR(500) DEFAULT 'openid,profile,email',
  BUTTON_LABEL VARCHAR(200) DEFAULT 'Mit Keycloak anmelden',
  BUTTON_ICON VARCHAR(100) DEFAULT 'pi pi-sign-in',
  AUTO_CREATE_USERS BOOLEAN DEFAULT FALSE,
  DEFAULT_ROLES VARCHAR(500) DEFAULT 'user',
  DEFAULT_MANDAT VARCHAR(100) DEFAULT 'default',
  USERNAME_ATTRIBUTE VARCHAR(100) DEFAULT 'email',
  CREATED_BY VARCHAR(255),
  CREATED_DATE TIMESTAMP,
  LAST_MODIFIED_BY VARCHAR(255),
  LAST_MODIFIED_DATE TIMESTAMP
);
