CREATE TABLE IF NOT EXISTS SETUP_CONFIG (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANDAT VARCHAR(100) NOT NULL,
    AUTOLOGIN_ENABLED BOOLEAN DEFAULT FALSE NOT NULL,
    OIDC_AUTO_REDIRECT_ENABLED BOOLEAN DEFAULT FALSE NOT NULL,
    OIDC_AUTO_REDIRECT_CONFIG_ID BIGINT,
    CREATED_BY VARCHAR(255),
    CREATED_DATE TIMESTAMP,
    LAST_MODIFIED_BY VARCHAR(255),
    LAST_MODIFIED_DATE TIMESTAMP,
    CONSTRAINT UK_SETUP_CONFIG_MANDAT UNIQUE (MANDAT)
);
