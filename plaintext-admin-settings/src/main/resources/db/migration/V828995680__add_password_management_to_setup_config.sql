-- Create SETUP_CONFIG table if it does not exist (may have been created by JPA DDL-auto)
CREATE TABLE IF NOT EXISTS SETUP_CONFIG (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANDAT VARCHAR(100) NOT NULL,
    AUTOLOGIN_ENABLED BOOLEAN NOT NULL DEFAULT FALSE,
    OIDC_AUTO_REDIRECT_ENABLED BOOLEAN NOT NULL DEFAULT FALSE,
    OIDC_AUTO_REDIRECT_CONFIG_ID BIGINT,
    PASSWORD_MANAGEMENT_ENABLED BOOLEAN NOT NULL DEFAULT TRUE,
    CREATED_BY VARCHAR(255),
    CREATED_DATE TIMESTAMP,
    LAST_MODIFIED_BY VARCHAR(255),
    LAST_MODIFIED_DATE TIMESTAMP
);

